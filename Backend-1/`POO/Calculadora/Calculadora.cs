
namespace calculadora
{
    public class Calculadora
    {
        public float num1;

        public float num2;



        public float somar(){

        return this.num1 + this.num2;
    }


    public float subtrair(){
        return this.num1 - this.num2;
    }
    

    public float multiplicar(){
        return this.num1 * this.num2;
    }

    public float dividir(){
        return this.num1 / this.num2;
    }








    }  
}

